package unisave2006.data;

public class MeasurementFormatException extends RuntimeException {

    private static final long serialVersionUID = 6667057204841080821L;

    public MeasurementFormatException() {
        super();
    }

    public MeasurementFormatException(String message, Throwable cause) {
        super(message, cause);
    }

    public MeasurementFormatException(String message) {
        super(message);
    }

    public MeasurementFormatException(Throwable cause) {
        super(cause);
    }
    
    
}
